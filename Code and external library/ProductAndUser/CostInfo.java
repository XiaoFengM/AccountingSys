package ProductAndUser;

import java.util.Date;

public class CostInfo {
    private int id; // Primary key of cost
    private double value;// The value of cost
    private Date date;// The date of cost
    private String note;//The note of cost

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
}
